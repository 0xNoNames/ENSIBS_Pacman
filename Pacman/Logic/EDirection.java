package Pacman.Logic;

/**
 * EDirection représente les différentes direction de déplacement des Entite
 * 
 * @author Louis-Baptiste Sobolewski
 */
public enum EDirection {
    /**
     * Vers le nord/haut de la fenêtre
     */
    NORD,

    /**
     * Vers le sud/bas de la fenêtre
     */
    SUD,

    /**
     * Vers l'est/droite de la fenêtre
     */
    EST,

    /**
     * Vers l'ouest/gauche de la fenêtre
     */
    OUEST
}
